let a = 10;

//operadores relacionales
console.log(a > 5); //mayor
console.log(a >= 5); //mayor o igual
console.log(a <= 5); //menor o igual
console.log(a < 5); //menor
//operadores igualdad
console.log(a != 10); //diferente
console.log(a == 10); //igual de valor
console.log(a === 5); //igual de valor y de tipo
console.log(a !== 5); //diferenete de valor y tipo